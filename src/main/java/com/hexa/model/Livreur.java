package com.hexa.model;

public class Livreur {

//-------------------------------------------------------------------------------------------------------------------------

	private int id;

//-------------------------------------------------------------------------------------------------------------------------

	public Livreur(int id) {
		this.id = id;
	}

//-------------------------------------------------------------------------------------------------------------------------

	/**
	 * @return int
	 */
	public int getId() {
		return id;
	}

//-------------------------------------------------------------------------------------------------------------------------

	/**
	 * @return String
	 */
	public String toString() {
		return "id : " + id;
	}
}